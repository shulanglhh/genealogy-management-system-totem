package pedi.pageModel;

public class Newhideman {
	private int pid;
	private String name;
	private String sex;
	private String father;
	private Short ggen;
	private String hiddenchild;
	private String inbiology;
	private String inrelation;
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSex() {
		return sex;
	}
	public void setSex(String sex) {
		this.sex = sex;
	}
	public String getFather() {
		return father;
	}
	public void setFather(String father) {
		this.father = father;
	}
	public Short getGgen() {
		return ggen;
	}
	public void setGgen(Short ggen) {
		this.ggen = ggen;
	}
	public String getHiddenchild() {
		return hiddenchild;
	}
	public void setHiddenchild(String hiddenchild) {
		this.hiddenchild = hiddenchild;
	}
	public String getInbiology() {
		return inbiology;
	}
	public void setInbiology(String inbiology) {
		this.inbiology = inbiology;
	}
	public String getInrelation() {
		return inrelation;
	}
	public void setInrelation(String inrelation) {
		this.inrelation = inrelation;
	}
}
